	<h2>Hacksaw Ridge</h2>
	<p>
	An unbelievable true story about a soldier who refused to use a gun after signing up for war, due to 
	his religious believe. This caused distrust and hate from the men who were in training around him as 
	they felt he would be not be able to help them in battle. Sadness surrounding the movie as private Doss 
	might get imprisoned for his rejection to use a weapon until his father steps in. Interested as to what 
	Doss will do on the battle ground, the viewer is left wondering. This is until Doss fearlessly shows 
	courage when other men are injured around him were filled with fear of death. Private Doss never gave 
	up and in the end when men were ready to go back up, they would not move until they had Doss with them 
	as he was what gave them hope. A film that portrays a strong message about war and beliefs. 
	</p>
	<p>Rating: 8</p>
	</div>
	<div>
	<h2>Fantastic Beasts and Where to Find Them</h2>
	<p>
	A spinoff of the Harry Potter series into the area of magical creatures from a wizard called Newt Scamander 
	who was kicked out of Hogwarts. Traveling to New York for a brief visit until he runs into trouble and some 
	of his magical creatures get loose in the city causing for him to acquire help from some American wizards. 
	With other mysterious magical events, his creatures are not the only thing he ends up having to deal with. 
	From being imprisoned to weaselling his way out and eventually helping the wizards of New York to regain 
	control and imprison an escaped dangerous rogue wizard. As much as it was like Harry Potters wizardry, it 
	showed another side to the wizarding world in which Harry Potter was apart off. Also very Entertaining and 
	comical. 
	</p>
	<p>Rating: 7</p>
	</div>
